public class Figura4troRaya{
	 
	 private int x, y, ancho, alto;
	 
	 public Figura4troRaya(){
		 this.x=0;
		 this.y=0;
		 this.ancho=0;
		 this.alto=0;
	 }
	 
	 public Figura4troRaya(int x, int y, int ancho, int alto){
		 
		 this.x=x;
		 this.y=y;
		 this.ancho=ancho;
		 this.alto=alto;
	 }
	 
	 public void setX(int x){
		 this.x=x;
	 }
	 public int getX(){
		 return this.x;
	 }
	 
	 public void setY(int y){
		 this.y=y;
	 }
	 public int getY(){
		 return this.y;
	 }
	 
	 public void setAncho(int ancho){
		 this.ancho=ancho;
	 }
	 public int getAncho(){
		 return this.ancho;
	 }
	 
	 public void setAlto(int alto){
		 this.alto=alto;
	 }
	 public int getAlto(){
		 return this.alto;
	 }
	 
	 
 
 
 }//class

